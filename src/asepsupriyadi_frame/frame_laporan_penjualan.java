    private void btn_generate_pdfActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_generate_pdfActionPerformed
        // TODO add your handling code here:
        List<ModelPenjualan> penjualanList = new ArrayList<>();

        int rowCount = model.getRowCount();
        for (int i = 0; i < rowCount; i++) {
            String nomorFaktur = model.getValueAt(i, 0).toString();
            String kdBarang = model.getValueAt(i, 1).toString();
            String namaBarang = model.getValueAt(i, 2).toString();
            Double hargaSatuan = Double.parseDouble(model.getValueAt(i, 3).toString());
            int qty = Integer.parseInt(model.getValueAt(i, 4).toString());
            String tglTransaksi = model.getValueAt(i, 5).toString();
            Double totalPenjualan = Double.parseDouble(model.getValueAt(i, 6).toString());

            penjualanList.add(new ModelPenjualan(nomorFaktur, kdBarang, namaBarang, hargaSatuan, qty, totalPenjualan, tglTransaksi));
        }

        Date reqStartDate = (Date) txt_startDate.getValue();
        Date reqEndDate = (Date) txt_endDate.getValue();
        SimpleDateFormat dateFormat = new SimpleDateFormat("dd-MMM-yyyy-HH-mm");

        String formattedStartDate = dateFormat.format(reqStartDate).toUpperCase();
        String formattedEndDate = dateFormat.format(reqEndDate).toUpperCase();

        String namaFile = "laporan_penjualan_pt-angin-ribut_" + formattedStartDate + ".pdf";
        String filePath = "C:\\Users\\TUF\\Documents\\Kuliah\\Object oriented programming 2\\hasil_generate_laporan\\" + namaFile;

        try {
            if (penjualanList.isEmpty()) {
                JOptionPane.showMessageDialog(null, "Tidak ada data tersedia !", "PT Angin Ribut", JOptionPane.INFORMATION_MESSAGE);
            } else {
                PDFGenerator.generateSalesReport(filePath, penjualanList, formattedStartDate, formattedEndDate);
                JOptionPane.showMessageDialog(null, "Laporan berhasil dibuat !", "PT Angin Ribut", JOptionPane.INFORMATION_MESSAGE);
            }
        } catch (Exception ex) {
            System.out.println(ex);
            JOptionPane.showMessageDialog(null, "Terjadi kesalahan !", "PT Angin Ribut", JOptionPane.INFORMATION_MESSAGE);
            Logger.getLogger(frame_laporan_penjualan.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_btn_generate_pdfActionPerformed

