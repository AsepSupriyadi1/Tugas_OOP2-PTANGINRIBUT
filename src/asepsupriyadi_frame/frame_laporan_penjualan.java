    private void loadData(Date reqStartDate, Date reqEndDate) {
        model.getDataVector().removeAllElements();
        model.fireTableDataChanged();
        Date startDate = (Date) txt_startDate.getValue();
        Date endDate = (Date) txt_endDate.getValue();
        SimpleDateFormat sdf = new SimpleDateFormat("yyyy-MM-dd");
        String startDateStr = sdf.format(startDate);
        String endDateStr = sdf.format(endDate);

        try {
            Connection c = koneksi_asepsupriyadi.getKoneksi();
            Statement s = c.createStatement();
            String query = "SELECT * FROM tb_penjualan WHERE tgl_penjualan BETWEEN '" + startDateStr + "' AND '" + endDateStr + "'";
            System.out.println(query);
            ResultSet r = s.executeQuery(query);
            while (r.next()) {
                Object[] o = new Object[7];
                o[0] = r.getString("no_faktur");
                o[1] = r.getString("kd_barang");
                o[2] = r.getString("nama_barang");
                o[3] = r.getString("hsatuan");
                o[4] = r.getString("jumlah_jual");
                o[5] = r.getString("tgl_penjualan");
                o[6] = r.getString("harga");
                model.addRow(o);
            }
            r.close();
            s.close();
        } catch (SQLException e) {
            System.out.println(e.getMessage());
            System.out.println("Terjadi kesalahan saat load data !");
            JOptionPane.showMessageDialog(null, "terjadi kesalahan");
        }
    }

    private void hitungTotalPendapatan() {
        int totalHarga = 0;
        int rowCount = model.getRowCount();
        for (int i = 0; i < rowCount; i++) {
            double harga = Integer.parseInt(model.getValueAt(i, 6).toString());
            totalHarga += harga;
        }
        label_total.setText("" + totalHarga);
    }

